public class Pratik_36_III {
    public static void main(String[] args){
        for(int i=5;i>=1;i--)//Outer for loop for rows
        {
            for(int j=1;j<=i;j++)//inner for loop for coloum
            {
                System.out.print("8");
            }
            System.out.println();
        }
    }
    
}
